    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String nombreProducto = txtBuscar2.getText(); // Obtener el texto del JTextField

        ImageIcon imagen = arbol.buscar(nombreProducto);

        // Redimensionar la imagen al tamaño del JLabel
        Image img = imagen.getImage();
        ImageIcon imagenAjustada = new ImageIcon(img.getScaledInstance(labelImagen.getWidth(), labelImagen.getHeight(), Image.SCALE_SMOOTH));
        
        labelImagen.setIcon(imagenAjustada);
        
        txtNombre.setText(txtBuscar2.getText());
        txtCodigo.setText(String.valueOf(arbolCodigo.buscar(nombreProducto)));
        txtStock.setText(String.valueOf(arbolStock.buscar(nombreProducto)));
        txtPrecioCompra.setText(String.valueOf(arbolPrecioCompra.buscar(nombreProducto)));
        txtPrecioVenta.setText(String.valueOf(arbolPrecioVenta.buscar(nombreProducto)));
        txtDescripcion.setText(arbolDescripcion.buscar(nombreProducto));
        txtDistribuidor.setText(arbolDistribuidor.buscar(nombreProducto));
        
        System.out.println("Lista: " + arbol.recorridoInOrden());
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        String nombre = txtNombre.getText();
        arbol.eliminar(nombre);
        arbolCodigo.eliminar(nombre);
        arbolPrecioCompra.eliminar(nombre);
        arbolPrecioVenta.eliminar(nombre);
        arbolStock.eliminar(nombre);
        arbolDistribuidor.eliminar(nombre);
        arbolDescripcion.eliminar(nombre);
        
        System.out.println("Lista: " + FormularioVenta.arbol.recorridoInOrden());
        
    }//GEN-LAST:event_eliminarActionPerformed

