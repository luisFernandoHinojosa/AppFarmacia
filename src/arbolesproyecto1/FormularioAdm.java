    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        modeloTabla = (DefaultTableModel) tablaADM.getModel();
        String  nombreProducto = txtNombre.getText();//nombre del producto
        int codigo = Integer.parseInt(txtCodigo.getText());//codigo del producto
        ImageIcon imagen = (ImageIcon) labelImagen.getIcon();//imagen del producto
        int precioCompra = Integer.parseInt(txtPrecioCompra.getText());//precio del producto
        int precioVenta = Integer.parseInt(txtPrecioVenta.getText());
        int stock = Integer.parseInt(txtStock.getText());//cantidad de cada producto
        String distribuidor = txtDistribuidor.getText();//distribuidora del producto
        String descripcion = txtDescripcion.getText();//descripcion de cada producto
        
        
        arbol.insertar(nombreProducto,imagen);
        arbolCodigo.insertar(nombreProducto, codigo);
        arbolPrecioCompra.insertar(nombreProducto, precioCompra);
        arbolPrecioVenta.insertar(nombreProducto, precioVenta);
        arbolStock.insertar(nombreProducto, stock);
        arbolDistribuidor.insertar(nombreProducto, distribuidor);
        arbolDescripcion.insertar(nombreProducto, descripcion);
        
        Object[] fila = {nombreProducto, codigo, stock, precioCompra, precioVenta, distribuidor };
        modeloTabla.addRow(fila);
        
        System.out.println("recorrido" + arbol.recorridoInOrden());
        System.out.println("recorrido arbol codigo" + arbolCodigo.recorridoInOrden());
        System.out.println("recorrido arbol precio compra" + arbolPrecioCompra.recorridoInOrden());
        System.out.println("recorrido arbol precio venta" + arbolPrecioVenta.recorridoInOrden());
        System.out.println("recorrido arbol stock" + arbolStock.recorridoInOrden());
        System.out.println("recorrido arbol distribuidor" + arbolDistribuidor.recorridoInOrden());
        System.out.println("recorrido arbol descripcion" + arbolDescripcion.recorridoInOrden());
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImagenActionPerformed
        String ruta = "";
        JFileChooser archivoImagen = new JFileChooser();
        FileNameExtensionFilter filtrar = new FileNameExtensionFilter("jpg, png, git", "jpg", "png", "gif");
        archivoImagen.setFileFilter(filtrar);
        int validar = archivoImagen.showOpenDialog(this);
        if (validar == JFileChooser.APPROVE_OPTION) {
            ruta = archivoImagen.getSelectedFile().getPath();
            Image imagen = new ImageIcon(ruta).getImage();
            ImageIcon miImagen = new ImageIcon(imagen.getScaledInstance(labelImagen.getWidth(), labelImagen.getHeight(), Image.SCALE_SMOOTH));
            labelImagen.setIcon(miImagen);

        }
    }//GEN-LAST:event_btnImagenActionPerformed

